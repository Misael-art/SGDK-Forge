void _segalogo( void )
{
   VDP_waitVSync();
   SYS_disableInts();
   VDP_setEnable(FALSE);
   VDP_drawImageEx(BG_B, &bgx_sega_logo, TILE_ATTR_FULL(PAL0, TRUE, FALSE, FALSE, 0), 0, 0, TRUE, FALSE);
   VDP_setEnable(TRUE);
   SYS_enableInts();
   VDP_waitVSync();

   u8 frame;
   for ( frame=0; frame<MAX_FRAMES; frame++ )
	{
		PAL_setPalette( PAL0, gif[frame].pal, DMA);

		u8 i;
		for( i=0; i<gif[frame].delay; i++ )
		{VDP_waitVSync();}

		if (frame == 21) {XGM_startPlay(xgm_sega_logo); }
	}
}
